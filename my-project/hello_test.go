package main

import "testing"

func TestHello(t *testing.T) {
	t.Run("dizendo oi para as pessoas", func(t *testing.T) {
		got := Hello("Chris")
		want := "Hello, Chris"

		if got != want {
			t.Errorf("got %q want %q", got, want)
		}
	})
	t.Run("diga olá mundo quando uma string vazia é fornecida", func(t *testing.T) {
		got := Hello("")
		want := "Hello, World"

		if got != want {
			t.Errorf("got %q want %q", got, want)
		}
	})
}
